#Diferença entre linguagens formais e naturais:

#Linguagens Formais: Usadas em matemática, lógica e programação, seguem regras rigorosas e precisas, sem espaço para ambiguidades. Exemplo: Python, Java.

#Linguagens Naturais: São os idiomas falados pelas pessoas, como português e inglês. São flexíveis e podem ser ambíguas, permitindo múltiplas interpretações.

#Três características que distinguem Python:

#Sintaxe Simples e Clara: Facilita a leitura e escrita de código.
#Tipagem Dinâmica: Não exige declaração explícita de tipos de dados.
#Bibliotecas Amplas: Possui uma vasta coleção de bibliotecas prontas para uso em diversas áreas.